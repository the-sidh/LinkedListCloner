package com.sid.lista.node;

public class Node {

	private Node next;
	private Node random;
	private String value;
	
	public Node getNext() {
		return next;
	}
	public void setNext(Node next) {
		this.next = next;
	}
	public Node getRandom() {
		return random;
	}
	public void setRandom(Node random) {
		this.random = random;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
	
	
	
}
